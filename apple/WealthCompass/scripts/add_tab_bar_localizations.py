XCSTRINGS = ROOT / "Sources/Shared/Resources/Localizable.xcstrings"
SWIFT_OUT = ROOT / "Sources/Shared/Services/TabBarLabelResolver.swift"
MAX_TAB_LENGTH = 10

TAB_KEYS = {
    "dashboard": "Dashboard",
    "cashFlow": "Cash Flow",
    "investments": "Investments",
    "crypto": "Crypto",
    "settings": "Settings",
}

